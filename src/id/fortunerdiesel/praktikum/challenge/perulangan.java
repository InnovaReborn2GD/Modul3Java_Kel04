package id.fortunerdiesel.praktikum.challenge;

public class perulangan {
    public static void main(String[]args) {
        System.out.println("ini dia perulangan do while:");
        int i = 1;
        do {
            System.out.print(i + " ");
            i++;
        } while (i <= 25);

        System.out.println("\nini dia perulangan while:");
        int j = 1;
        while (j <= 25) {
            System.out.print(j + " ");
            j++;
        }
    }
}
